A, S, B = input().split()
A = int(A)
B = int(B)
 
# Calculate and print the result based on the operator
if S == '+':
    result = A + B
elif S == '-':
    result = A - B
elif S == '*':
    result = A * B
else:  # Assuming S can only be '+'/'-'/'*'/,'
    result = A // B  # Perform integer division for division operation
 
print(result)